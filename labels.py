def assign_labels(nodes):
    if len(nodes) > len(VOICE_LABELS):
        print(
            f"warning: {len(nodes)} nodes but only {len(VOICE_LABELS)} labels available"
        )
    shuffled = random.sample(VOICE_LABELS, len(nodes))
    return {label: node for label, node in zip(shuffled, nodes)}
